class Product {
  final int? id;
  final String? product_name;
  final String? product_type;
  final int? amount;
  final int? price;
  final bool? product_status;
  Product({
    required this.id,
    required this.product_name,
    required this.product_type,
    required this.amount,
    required this.price,
    required this.product_status,
  });
}
